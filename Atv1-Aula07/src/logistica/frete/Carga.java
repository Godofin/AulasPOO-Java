package logistica.frete;

public class Carga {
private long eixos;
private int peso;
private double valor;

public long getEixos() {
	return eixos;
}
public int getPeso() {
	return peso;
}
public double getValor() {
	return valor;
}
public void setEixos(long eixos) {
	this.eixos = eixos;
}
public void setPeso(int peso) {
	this.peso = peso;
}
public void setValor(double valor) {
	this.valor = valor;
}


}
